	<h2>Sobre nós</h2>
	<div class="article-body">
		<p>Somos uma equipe dedicada a saúde mental de nossos pacientes e de não pacientes cadastrados no nosso sistema.
		Na clínica e através de telemedicina, oferecemos serviços do tipo acompanhamento psiquiátrico com farmacoterapia (caso necessário) e suporte psicológico. </p>
		<p>A clínica foi fundada em julho de 2014 por um psiquiatra experiente e reconhecido, com expectativa de melhorar o suporte através de uma equipe integrada, que tivesse como foco absoluto o paciente, dentro de uma estratégia terapêutica humanizada.</p>
